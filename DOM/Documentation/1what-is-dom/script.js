//What is Node

/*What is a Node in the DOM?
In the Document Object Model (DOM), a node is a fundamental unit that represents a part of the document (e.g., an element, attribute, or text). The DOM is structured as a tree of nodes, where each node can have parent, child, and sibling relationships.
 */

